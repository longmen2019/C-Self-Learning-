#include <iostream>
#include <cmath>

using namespace std;

int main()
{
  int num1 ,num2;
  //This will take input for the first number
  cout << "Enter first number:";
  //This will store the first input
  cin >> num1;

  //This wil take input for the second number
  cout << "Enter second number:";

  //This will store the second input
  cin >> num2;

  //This will do the addition between num1 and num2
  cout << num1 + num2; 

  return 0; 
}

